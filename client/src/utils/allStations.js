// Get all stations as a single array
export const getAllStations = () => {
  return [
    ...railwayStations.western,
    ...railwayStations.central,
    ...railwayStations.harbour,
    ...railwayStations.metro1,
    ...railwayStations.metro3,
  ];
};

// Get stations for a specific line
export const getStationsByLine = (line) => {
  return railwayStations[line.toLowerCase()] || [];
};

// Get polyline coordinates for each line
export const getLineCoordinates = (line) => {
  const stations = getStationsByLine(line);
  return stations.map((s) => [s.lat, s.lng]);
};

